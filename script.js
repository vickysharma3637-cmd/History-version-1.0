// Show question
function showQuestion() {
  const q = questions[current];
  document.getElementById("question").innerText = q.question;

  const opts = document.getElementById("options");
  opts.innerHTML = "";
  q.options.forEach((opt, i) => {
    const btn = document.createElement("button");
    btn.innerText = opt;
    btn.onclick = () => selectAnswer(i);
    if (userAnswers[current] === i) btn.style.backgroundColor = "#d1e7dd";
    opts.appendChild(btn);
  });

  document.getElementById("progress").innerText =
    `ЁЯСЛ Welcome, ${learnerName}! | рдкреНрд░рд╢реНрди ${current + 1} / ${questions.length}`;
  document.getElementById("prev-btn").disabled = current === 0;
  document.getElementById("next-btn").disabled = current === questions.length - 1;

  renderNavigator();
  saveProgress();
}

// Select answer
function selectAnswer(selected) {
  if (timeExpired) return;
  userAnswers[current] = selected;
  const correct = questions[current].correct;
  if (selected === correct) score += 2;
  else score -= 0.5;
  saveProgress();
  showQuestion();
}

// Navigation
function goToPrevious() {
  if (timeExpired || current === 0) return;
  current--;
  saveProgress();
  showQuestion();
}

function goToNext() {
  if (timeExpired) return;
  if (current < questions.length - 1) {
    current++;
    saveProgress();
    showQuestion();
  } else {
    clearInterval(timer);
    showResult();
  }
}

// Review mark
function toggleReview() {
  if (timeExpired) return;
  markedForReview[current] = !markedForReview[current];
  saveProgress();
  renderNavigator();
}

// Navigator
function renderNavigator() {
  const panel = document.getElementById("navigator-panel");
  panel.innerHTML = "";
  questions.forEach((_, i) => {
    const btn = document.createElement("button");
    btn.innerText = i + 1;
    if (markedForReview[i]) btn.classList.add("marked");
    btn.onclick = () => {
      if (timeExpired) return;
      current = i;
      saveProgress();
      showQuestion();
    };
    panel.appendChild(btn);
  });
}

