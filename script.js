let current = 0;
let score = 0;
let userAnswers = [];
let learnerName = "";
let timer;
let markedForReview = [];

function shuffle(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
}

function saveProgress() {
  const progressData = {
    current,
    score,
    userAnswers,
    questions,
    learnerName,
    markedForReview
  };
  localStorage.setItem("quizProgress", JSON.stringify(progressData));
}

function loadProgress() {
  const saved = localStorage.getItem("quizProgress");
  if (saved) {
    const data = JSON.parse(saved);
    current = data.current;
    score = data.score;
    userAnswers = data.userAnswers;
    questions = data.questions;
    learnerName = data.learnerName;
    markedForReview = data.markedForReview || Array(questions.length).fill(false);
    document.getElementById("welcome-screen").style.display = "none";
    document.querySelector(".container").style.display = "block";
    showQuestion();
  }
}

function startQuiz() {
  const nameInput = document.getElementById("learner-name").value.trim();
  if (!nameInput) {
    alert("рдХреГрдкрдпрд╛ рдЕрдкрдирд╛ рдирд╛рдо рдбрд╛рд▓реЗрдВред");
    return;
  }
  learnerName = nameInput;
  localStorage.setItem("learnerName", learnerName);
  document.getElementById("welcome-screen").style.display = "none";
  document.querySelector(".container").style.display = "block";
  document.getElementById("progress").innerText = `ЁЯСЛ Welcome, ${learnerName}!`;
  initQuiz();
}

function initQuiz() {
  shuffle(questions);
  markedForReview = Array(questions.length).fill(false);
  showQuestion();
}

function showQuestion() {
  const q = questions[current];
  document.getElementById("question").innerText = q.question;
  const opts = document.getElementById("options");
  opts.innerHTML = "";

  q.options.forEach((opt, i) => {
    const btn = document.createElement("button");
    btn.innerText = opt;
    btn.onclick = () => selectAnswer(i);
    if (userAnswers[current] === i) {
      btn.style.backgroundColor = "#d1e7dd";
    }
    opts.appendChild(btn);
  });

  document.getElementById("progress").innerText = `ЁЯСЛ Welcome, ${learnerName}! | рдкреНрд░рд╢реНрди ${current + 1} / ${questions.length}`;
  document.getElementById("prev-btn").disabled = current === 0;
  document.getElementById("next-btn").disabled = current === questions.length - 1;

  renderNavigator();
  saveProgress();
}

function selectAnswer(selected) {
  userAnswers[current] = selected;
  const correctAnswer = questions[current].correct;
  if (selected === correctAnswer) {
    score += 2;
  } else {
    score -= 0.5;
  }
  showQuestion();
  saveProgress();
}

function goToPrevious() {
  if (current > 0) {
    current--;
    showQuestion();
  }
}

function goToNext() {
  if (current < questions.length - 1) {
    current++;
    showQuestion();
  } else {
    clearInterval(timer);
    showResult();
  }
}

function toggleReview() {
  markedForReview[current] = !markedForReview[current];
  alert(markedForReview[current] ? "Marked for review!" : "Unmarked.");
  saveProgress();
  renderNavigator();
}

function renderNavigator() {
  const panel = document.getElementById("navigator-panel");
  panel.innerHTML = "";
  questions.forEach((_, i) => {
    const btn = document.createElement("button");
    btn.innerText = i + 1;
    btn.onclick = () => {
      current = i;
      showQuestion();
    };
    if (markedForReview[i]) btn.style.border = "2px solid orange";
    panel.appendChild(btn);
  });
}

